package com.usfEmpMgmt;

public class PerformMgr {
	private String empId;
	private String empName;
	private String ratingPeriod;
	private String appraiser;
	private String empRating;
	
	public String getRatingPeriod() {
		return ratingPeriod;
	}
	public void setRatingPeriod(String ratingPeriod) {
		this.ratingPeriod = ratingPeriod;
	}
	public String getAppraiser() {
		return appraiser;
	}
	public void setAppraiser(String appraiser) {
		this.appraiser = appraiser;
	}
	public String getEmpRating() {
		return empRating;
	}
	public void setEmpRating(String empRating) {
		this.empRating = empRating;
	}
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	
	
}
